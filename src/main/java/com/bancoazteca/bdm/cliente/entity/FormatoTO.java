package com.bancoazteca.bdm.cliente.entity;

public class FormatoTO {
	
	private String nombre;
	private String dato;
	private String longitud;
	private String requerido;
	private String formato;
	private String ejemplo;	
	private String observacion;
	private String claseComplejo;
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDato() {
		return dato;
	}
	public void setDato(String dato) {
		this.dato = dato;
	}
	public String getLongitud() {
		return longitud;
	}
	public void setLongitud(String longitud) {
		this.longitud = longitud;
	}
	public String getRequerido() {
		return requerido;
	}
	public void setRequerido(String requerido) {
		this.requerido = requerido;
	}
	public String getFormato() {
		return formato;
	}
	public void setFormato(String formato) {
		this.formato = formato;
	}
	public String getEjemplo() {
		return ejemplo;
	}
	public void setEjemplo(String ejemplo) {
		this.ejemplo = ejemplo;
	}
	public String getObservacion() {
		return observacion;
	}
	public void setObservacion(String observacion) {
		this.observacion = observacion;
	}
	public String getClaseComplejo() {
		return claseComplejo;
	}
	public void setClaseComplejo(String claseComplejo) {
		this.claseComplejo = claseComplejo;
	}
	
	

}
